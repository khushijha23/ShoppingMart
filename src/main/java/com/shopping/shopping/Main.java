package com.shopping.shopping;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.stage.Stage;

import java.io.IOException;

public class Main extends Application {
    @Override
    public void start(Stage stage) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("design.fxml"));
        Scene scene = new Scene(fxmlLoader.load(), 1250, 750);

        // Controller controller = new Controller(); // Remove this line

        stage.setTitle("Shopping Mart Management");
        stage.setResizable(false);
        stage.setScene(scene);
        stage.show();
    }

    public static void main(String[] args) {
        launch();
    }
}
